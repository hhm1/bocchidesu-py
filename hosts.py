with open('C:\Windows\System32\drivers\etc\hosts','r')as f:
    lines = f.readlines()
ge = ['#ms','Beyond']
new = ''
for line in lines:
    mode = True
    for i in ge:
        if i in line:
            mode = False
            break
    if mode:
        new += line
with open('C:\Windows\System32\drivers\etc\hosts','w')as f:
    f.write(new)





